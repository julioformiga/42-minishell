#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	len_src;

	len_src = 0;
	while (src[len_src] != '\0')
		len_src++;
	if (size <= 0)
		return (len_src);
	i = -1;
	while (i++, i < size - 1 && i < len_src)
		dst[i] = src[i];
	dst[i] = '\0';
	return (len_src);
}
