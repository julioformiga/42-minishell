#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t n)
{
	char		*pdst;
	const char	*psrc;

	pdst = (char *) dst;
	psrc = (char *) src;
	if (src < dst)
	{
		while (n--)
			pdst[n] = psrc[n];
		return (dst);
	}
	ft_memcpy(pdst, psrc, n);
	return (dst);
}
